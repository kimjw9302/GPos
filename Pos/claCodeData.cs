		/// <summary>
		/// 지정된 문자를 차수로 바꿔준다.
		/// </summary>
		/// <param name="strData">지정된 문자</param>
		/// <param name="intType"></param>
		/// <returns></returns>
		public int intReTurnN(string strData, int intType)
		{
			switch (intType)
			{
				case 1:
					return -1;

				case 2:
					return -1;

				default:	//A타입
					{
						int intTemp = strAType.Length;
						intTemp = intTemp / 2;

						for (int i = 0; i <= intTemp - 1; i++)
						{
							//들어온 문자열과 타입문자열을 비교한다
							if (strAType[i, 0] == strData)
							{	//문자열과 일치하는 데이터가 있으면 수치를 리턴한다.
								return Convert.ToInt32(strAType[i, 1]);
							}
						}

						return -1;
					}
			}

